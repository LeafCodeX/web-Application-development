			<h2>Sport elektroniczny</h2>
			<p><b>Sport elektroniczny</b> to forma rywalizacji, w której
                przedmiotem działań zawodników są gry komputerowe. Zawody e-sportowe często przyjmują formę zorganizowanej wieloosobowej gry komputerowej, gdzie zawodowi
                gracze prowadzą rozgrywkę indywidualnie lub w drużynach, zebrani fizycznie lub przez Internet. Mimo tego, że zawody i inne formy rywalizacji stanowią od
                dawna część kultury gier komputerowych, przez dłuższy czas były traktowane jedynie rozrywkowo, rozgrywane przez amatorów. Dopiero wraz z początkiem XXI wieku,
                dzięki możliwości strumieniowania rozgrywek na żywo, zaczęły nabierać obecnej formy wraz z gwałtownym wzrostem ich popularności. Pojawili się zawodowi gracze
                oraz komentatorzy, specjalizujący się w danej grze. Szybki wzrost zainteresowania i znaczenia e-sportu spowodował, że już w drugiej dekadzie XXI wieku
                stanowił istotny czynnik w branży gier komputerowych, a twórcy aktywnie wspierali i współtworzyli go, przystosowując swoje gry i finansując turnieje.
				<br><br>
                Po wielu latach praktyki w rozgrywkach w ulubione gry, zagorzali gracze, którym udaje się bić rekordy i plasować się w czołówce najlepszych na świecie,
                mają szansę przenieść swoją pasję na zupełnie innym poziom. Kariera w e-sporcie nie należy do najłatwiejszych. Stała rywalizacja wywołuje sporą ilość stresu
                i wymaga godzin praktyki przed ekranami telewizora, monitora czy innego sprzętu. Jest to sytuacja bardzo zbliżona do tradycyjnego sportu, gdzie również trzeba
                systematycznie trenować. E-sport jest formą rywalizacji, gdzie przedmiotem są gry wideo, rozgrywki w takie gry jak Counter-Strike: Global Offensive, Fortnite
                czy League of Legends odbywają się w formie rekreacyjnej oraz w postaci turniejów. Obecnie sport elektroniczny jest bardzo popularny, wiele osób kibicuje swoim
                ulubionym drużynom online jak i w wielkich arenach widowiskowych. Zawodnicy mają swoich trenerów, psychologów oraz dokładnie wytyczone plany treningowe tak, jak
                u tradycyjnych sportowców - praktyka czyni mistrza. Nagrody w turniejach e-sportowych sięgają nawet 3 milionów dolarów! Tak było przypadku World Cupa w Fortnite,
                gdzie zwycięzca otrzymał właśnie taką nagrodę.
                <br><br>
                W 2019 roku oszacowano, że całkowita liczba regularnych widzów e-sportu wzrośnie do 454 milionów, a przychody wzrosną do ponad 1 miliarda USD, a rosnąca
                dostępność internetu i platform streamingowych, zwłaszcza YouTube i Twitch, stała się kluczowa dla rozwoju i promocji zawodów e-sportowych.
