			<h2>Historia</h2>
			<p>
				<b>E-sport</b> sięga swoją historią 1972 roku.
                Zorganizowany na terenie Uniwersytetu Stanford turniej miał być ciekawostką, skierowaną do studentów i pracowników
                naukowych instytutu. Rywalizacja dotyczyła umiejętności gry w Spacewar - prostą grę, w której jedynym zadaniem było kierowanie statkiem kosmicznym i strzelanie do
                przeciwników. Co ciekawe, dodatkową trudność wprowadzało pole grawitacyjne gwiazdy i bezwładność statków - te utrudnienia miały wprowadzić do gry jeszcze więcej emocji.
                Za początek gier multiplayer uznaje się jednak rok 1988, kiedy to swojej premiery doczekała się kultowa gra Netrek. Była to krzyżówka gry akcji i strategii czasu
                rzeczywistego, która pozwalała na zabawę w maksymalnie szesnaście osób, umożliwiając jednocześnie zdrowe współzawodnictwo przez sieć i przeglądanie listy dostępnych
                gier publicznych. Po drodze odbyło się jednak jeszcze jedno ważne wydarzenie, które miało prawdopodobnie największy wpływ na rozwój e-sportu. W 1980 roku Atari
                zorganizowało zawody w Space Invaders - to wydarzenie znane jest jako pierwszy turniej na większą skalę w dziedzinie gier wideo. Kolejnym istotnym wydarzeniem było
                Nintendo World Championships, turniej który odbył się w 1990 roku. Za zajęcie pierwszego miejsca, zwycięzca zgarnął dziesięć tysięcy dolarów, samochód, 40-calowy
                telewizor i złote trofeum!
                <br><br>
                Lata 90. to czas, w którym ogromną popularnością zaczynają cieszyć się komputery stacjonarne. Dostęp do Internetu sprawia, że gry nabierają nowego wymiaru. W końcu
                możliwa staje się rywalizacja online z ludźmi z całego świata. Oczywiście możliwości były ograniczone, jednak był to początek prawdziwych pojedynków i rywalizacji,
                w których gracz mógł zmierzyć się nie tylko z kolegą z podwórka. Koniec XX wieku to również premiery gier, które znamy do dzisiaj – Doom (1993), Warcraft II (1995),
                Quake (1996), Starcraft (1998) czy w końcu Counter-Strike (1999). Każda z tych gier oferowała rywalizację online, która w dużej mierze przyczyniła się do niezwykłej
                popularności tych tytułów. I tak na fali wirtualnej rywalizacji zaczynają tworzyć się pierwsze organizacje zajmujące się organizowaniem eventów e-sportowych.
                Powstaje między innymi: DreamHack (1994), Cyberathlete Professional League (1997), Turtle Entertainment (2000), World Cyber Games (2000) czy Electronic Sports World
                Cup (2003).
            </p>
