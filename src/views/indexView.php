			<h2>League of Legends - Rozgrywka</h2>
			<p>
			<b>League of Legends (LoL)</b>,
                powszechnie nazywany Ligą to drużynowa gra strategiczna, w której dwie drużyny składające się z pięciu lub trzech potężnych bohaterów walczą ze sobą, by zniszczyć bazę
                przeciwnika "Nexus". Aby to osiągnać drużyna musi oczyścić co najmniej jedną aleję. Drogę do wrogiej bazy blokują jednak budowle obronne, zwane wieżami i inhibitorami.
                W każdej alei znajdują się dokładnie trzy wieże i jeden inhibitor, a każdy Nexus jest broniony przez następne dwie wieże. Pomiędzy alejami znajdują się dżungla, w której
                można znaleźć neutralne potwory oraz rośliny. Najważniejsze potwory to Baron Nashor oraz Smoki. Zabicie ich zapewnia drużynie wyjątkowe wzmocnienia i może całkowicie
                zmienić przebieg gry. Bohaterowie rosną w siłę, zdobywając doświadczenie, dzięki któremu awansują na wyższe poziomy i gromadząc złoto, za które wraz z postępami
                w grze kupują coraz potężniejsze przedmioty. Pilnowanie tych dwóch elementów jest kluczem do zyskania przewagi nad wrogiem i zniszczenia jego bazy. Ponadto
                każdy z bohaterów posiada także pięć głównych umiejętności, dwa specjalne czary i mogą posiadać maksymalnie siedem przedmiotów jednocześnie.
                Odpowiednia kolejność rozwijania umiejętności, czarów przywoływacza oraz dobór przedmiotów pomoga drużynie odnieść sukces na arenie.
                <br><br>
                W trakcie rozgrywki należy także uważać na wiele innych czynników, takich jak np. przejrzystość mapy, rozmieszczenie przeciwników, jak i osiąganie pomniejszych
                celów, pokroju Smoka Żywiołów, Heralda czy Barona. League of Legends podzielone jest na trzy mapy: <b>Summoner's Rift(5v5)</b>, <b>Twisted Treeline(3v3)</b> oraz
                <b>Howling Abyss(ARAM)</b>, z czego właśnie to na tej pierwszej rozgrywane są największe turnieje na międzynarodowym poziomie.
                Dzięki swojej atrakcyjności i mechanice League of Legends szybko stało się najpopularniejszą grą e-sportową na świecie i zrzesza miliony graczy na całym globie.
                Riot Games nieustannie wspiera profesjonalnie rozgrywki na każdym poziomie - od międzynarodowych lig LCS, LEC czy LPL, po bardziej lokalne, pokroju polskiej Ultraligi.
                Wsród najbardziej znaczących turniejów można wyróżnić m. in.: Mistrzostwa Świata, czyli profesjonalny turniej e-sportowy rozgrywany corocznie, nieprzerwanie od 2011 roku,
                organizowany przez Riot Games jako zwieńczenie danego sezonu, Mid-Season Invitational, także coroczny turniej organizowany nieprzerwanie od 2015 roku. Jest on drugim
                najważniejszym turniejem zaraz po Mistrzostwach Świata. Ostatni typ turnieju to profesjonalne ligi regionalne, które organizowane są na poszczególnych kontynentach, a głównym
                sponsorem jest Riot Games. Do najbardziej znanych lig regionalnych zaliczyć można: LCS w Ameryce Północnej (League of Legends Championship Series), LEC	w Europie (League
                of Legends European Championship), LCK w Korei Południowej, LPL w Chinach i PCS w Azji Południowo-Wschodniej.
